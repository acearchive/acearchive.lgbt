class FileTypeIcon extends HTMLElement {
    connectedCallback() {
        const mediaType = this.getAttribute("media-type") || "application/octet-stream"
        const filename = this.getAttribute("filename") || ""

        let icon;
        let title;

        if (mediaType === "application/pdf" || filename.endsWith(".pdf")) {
            icon = pdfIcon;
            title = "PDF document";
        } else if (
            mediaType === "application/x-bibtex" || filename.endsWith(".bib")
            || mediaType === "application/x-research-info-systems" || filename.endsWith(".ris")
            || mediaType === "application/rdf+xml" || filename.endsWith(".rdf")
        ) {
            icon = referenceIcon;
            title = "Citation file";
        } else if (
            mediaType === "application/warc" || filename.endsWith(".warc")
            || filename.endsWith(".wacz")
        ) {
            icon = webArchiveIcon;
            title = "Web archive file";
        } else if (mediaType === "text/html" || filename.endsWith(".html") || filename.endsWith(".htm")) {
            icon = webPageIcon;
            title = "Web page";
        } else if (mediaType.startsWith("text/")) {
            icon = textIcon;
            title = "Text file";
        } else if (mediaType.startsWith("image/")) {
            icon = imageIcon;
            title = "Image file";
        } else if (mediaType.startsWith("video/")) {
            icon = videoIcon;
            title = "Video file";
        } else if (mediaType.startsWith("audio/")) {
            icon = audioIcon;
            title = "Audio file";
        } else {
            icon = fileIcon;
            title = "File";
        }

        const iconElement = document.createElement("span");
        iconElement.setAttribute("aria-hidden", true);
        iconElement.innerHTML = icon;

        const labelElement = document.createElement("span");
        labelElement.classList.add("visually-hidden");
        labelElement.innerHTML = title;

        this.setAttribute("title", title);
        this.setAttribute("data-bs-toggle", "tooltip");
        this.setAttribute("data-bs-placement", "top");

        this.appendChild(iconElement);
        this.appendChild(labelElement);

        new Tooltip(this)
    }
}

customElements.define("file-type-icon", FileTypeIcon);
